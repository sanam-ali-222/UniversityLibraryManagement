    private void getStudents(){
        try{
            BatchBean bean=(BatchBean)this.batchYearComboBox.getSelectedItem();
            if(bean==null)return;

            this.groupField.setText(bean.getGroupDesc());
            
            
            Vector v = DatabaseManager.getStudents(bean.getBatchId());
            this.studentNameList.setListData(v);
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }
    
        private void getDepartments(){
        try{
            
            Vector v = DatabaseManager.getDepartments();
            this.departmentComboBox.removeAllItems();
            
            for( int i=0; i<v.size(); i++ )
                this.departmentComboBox.addItem(v.elementAt(i));
            
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }


