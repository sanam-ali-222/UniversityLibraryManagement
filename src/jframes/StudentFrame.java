    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        StudentBean bean=(StudentBean)this.studentNameList.getSelectedValue();
        String stdName=this.studentNameField.getText();
        String fName=this.fNameField.getText();
        String surname=this.surnameField.getText();
        String rollNo=this.rollNoField.getText();
        String remarks=remarksArea.getText();

        if(stdName.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Student Name TextField is Empty. ");
           return;        
        }
        if(fName.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Father Name TextField is Empty. ");
           return;        
        }
        if(surname.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Surname TextField is Empty. ");
           return;        
        }
        if(rollNo.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Roll# TextField is Empty. ");
           return;        
        }
        try{
             int rows=DatabaseManager.updateStudent(bean.getStdId(),bean.getBatchId(), stdName, fName,surname, rollNo, remarks);
             if(rows>=1){
                 JOptionPane.showMessageDialog(this, rows+" record modified. ");
                 getStudents();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

    
