    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        StudentBean bean=(StudentBean)this.studentNameList.getSelectedValue();
        if(bean==null)return;
        
        try{
            int rows=DatabaseManager.deleteStudent(bean.getStdId());
            if(rows>=1){
                 JOptionPane.showMessageDialog(this, rows+" record removed. ");
                 getStudents();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_deleteActionPerformed

    
