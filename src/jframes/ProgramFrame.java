    private void getPrograms(){
        try{
            DepartmentBean bean=(DepartmentBean)this.departmentComboBox.getSelectedItem();
            if(bean==null)return;        
            Vector v = DatabaseManager.getPrograms(bean.getDeptId());
            this.programList.setListData(v);

        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }

    
    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        ProgramBean bean=(ProgramBean)this.programList.getSelectedValue();
        if(bean==null)return;
        
        String progName=this.progNameField.getText();
        String semDuration=this.semDurationField.getText();
        String remarks=remarksArea.getText();
       
        if(progName.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Program Name TextField is Empty. ");
           return;        
        }
        if(semDuration.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Semester Duration TextField is Empty. ");
           return;        
        }
        
        try{
             int rows=DatabaseManager.updateProgram(bean.getProgId(), bean.getDeptId(), progName, semDuration, remarks);
             if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record modified. ");
                 getDepartments();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

    
    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        DepartmentBean bean=(DepartmentBean)this.departmentComboBox.getSelectedItem();
        String progName=this.progNameField.getText();
        String semDuration=this.semDurationField.getText();
        String remarks=remarksArea.getText();
        
        if(progName.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Program Name TextField is Empty. ");
           return;        
        }
        else if(semDuration.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Semeter Duration TextField is Empty. ");
           return;        
        }
        
        try{
        
             int rows=DatabaseManager.addProgram(bean.getDeptId(),progName, semDuration, remarks);
             if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record saved. ");
                 getPrograms();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_addActionPerformed

        private void getDepartments(){
        try{
            
            Vector v = DatabaseManager.getDepartments();
            this.departmentComboBox.removeAllItems();
            
            for( int i=0; i<v.size(); i++ )
                this.departmentComboBox.addItem(v.elementAt(i));
            
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }
    
