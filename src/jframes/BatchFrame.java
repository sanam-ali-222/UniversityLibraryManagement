    private void getPrograms(){
        try{
            DepartmentBean bean=(DepartmentBean)this.departmentComboBox.getSelectedItem();
            if(bean==null)return;
            
            Vector v = DatabaseManager.getPrograms(bean.getDeptId());
            this.programComboBox.removeAllItems();
            
            for( int i=0; i<v.size(); i++ )
                this.programComboBox.addItem(v.elementAt(i));
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }

    
