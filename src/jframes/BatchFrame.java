    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        BatchBean bean=(BatchBean)this.batchYearList.getSelectedValue();
        if(bean==null)return;
        
        try{
            int rows=DatabaseManager.deleteBatch(bean.getBatchId());
            if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record removed. ");
                 getBatches();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }        
    }//GEN-LAST:event_deleteActionPerformed

    
