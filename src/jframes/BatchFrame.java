    private void batchYearListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_batchYearListValueChanged
        BatchBean bean = (BatchBean) batchYearList.getSelectedValue();
        if(bean==null)return;
        
        this.batchIdField.setText(""+bean.getBatchId());
        this.batchYearField.setText(""+bean.getBatchYear());
        this.remarksArea.setText(""+bean.getRemarks());
        
        this.groupComboBox.setSelectedItem(bean.getGroupDesc());    
    }//GEN-LAST:event_batchYearListValueChanged

    
    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        BatchBean bean=(BatchBean)this.batchYearList.getSelectedValue();
        if(bean==null)return;
        
        String batchYear=this.batchYearField.getText();
        String groupDesc=(String)this.groupComboBox.getSelectedItem();
        String remarks=remarksArea.getText();

        if(batchYear.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Program Name TextField is Empty. ");
           return;        
        }        
        try{
        
             int rows=DatabaseManager.updateBatch(bean.getBatchId(),bean.getProgId(), batchYear, groupDesc, remarks);
             if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record modified. ");
                 getBatches();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

    
    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        ProgramBean bean=(ProgramBean)this.programComboBox.getSelectedItem();
        String batchYear=this.batchYearField.getText();

        String groupDesc=(String)this.groupComboBox.getSelectedItem();
        String remarks=remarksArea.getText();

        if(batchYear.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Program Name TextField is Empty. ");
           return;        
        }        
        try{
        
             int rows=DatabaseManager.addBatch(bean.getProgId(), batchYear, groupDesc, remarks);
             if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record saved. ");
                 getBatches();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_addActionPerformed

        private void getDepartments(){
        try{            
            Vector v = DatabaseManager.getDepartments();
            this.departmentComboBox.removeAllItems();
            
            for( int i=0; i<v.size(); i++ )
                this.departmentComboBox.addItem(v.elementAt(i));
            
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }

    
