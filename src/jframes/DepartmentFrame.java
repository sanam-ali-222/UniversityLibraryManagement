    private void departmentListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_departmentListValueChanged
        DepartmentBean bean =(DepartmentBean) departmentList.getSelectedValue();
        if(bean==null)return;
        
        deptIdField.setText(""+bean.getDeptId());
        deptNameField.setText(""+bean.getDeptName());
        remarksArea.setText(""+bean.getRemarks()); 
    }//GEN-LAST:event_departmentListValueChanged

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
        DepartmentBean bean=(DepartmentBean)this.departmentList.getSelectedValue();
        if(bean==null)return;
        String deptName=this.deptNameField.getText();
        String remarks=remarksArea.getText();
                
        if(deptName.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Department Name TextField is Empty. ");
           return;        
        }
        
        try{        
             int rows=DatabaseManager.updateDepartment(bean.getDeptId() ,deptName, remarks);
             if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record modified. ");
                 getDepartments();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        String deptName=this.deptNameField.getText();
        String remarks=remarksArea.getText();
        
        
        if(deptName.trim().equals("")){
           JOptionPane.showMessageDialog(this, "Department Name TextField is Empty. ");
           return;        
        }        
       
        try{
             int rows=DatabaseManager.addDepartment(deptName, remarks);
             if(rows>=1){
                 javax.swing.JOptionPane.showMessageDialog(this, rows+" record saved. ");
                 getDepartments();
                 clearActionPerformed(evt);                                      
             }
        }catch(Exception ee){
           ee.printStackTrace();
           javax.swing.JOptionPane.showMessageDialog(this,"Error: "+ee.getMessage());
        }
                                       

    
    }//GEN-LAST:event_addActionPerformed

    private void getDepartments(){
        try{
            
            Vector v = DatabaseManager.getDepartments();
            departmentList.setListData(v);

        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"ERROR: "+ex );
        }
    }
    

